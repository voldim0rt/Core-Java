package com.tnmy;

public class Pattern01 {
    public static void main(String[] args) {
        int n = 5;
        int m = 5;
        for (int i = 1; i <= n; i++) {
            // System.out.print("*");
            for (int j = 1; j <= m; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }

}

